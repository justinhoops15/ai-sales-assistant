// Get commission % for a carrier product at agent's FFL level
export function getComp(carrierId, productName, fflLevel) {
  const carrier = COMP_GRID[carrierId];
  if (!carrier) return null;
  const product = carrier[productName];
  if (!product) return null;
  return product[fflLevel] ?? null;
}

// Get highest paying product for a carrier at agent's FFL level
export function getHighestCompProduct(carrierId, fflLevel) {
  const carrier = COMP_GRID[carrierId];
  if (!carrier) return null;
  let best = null;
  let bestComp = 0;
  for (const [productName, levels] of Object.entries(carrier)) {
    const comp = levels[fflLevel];
    if (comp && comp > bestComp) {
      bestComp = comp;
      best = { productName, comp };
    }
  }
  return best;
}

// Rank all products across all 10 carriers by comp at agent's FFL level
export function rankAllProductsByComp(fflLevel) {
  const ranked = [];
  for (const [carrierId, products] of Object.entries(COMP_GRID)) {
    for (const [productName, levels] of Object.entries(products)) {
      const comp = levels[fflLevel];
      if (comp) ranked.push({ carrierId, productName, comp });
    }
  }
  return ranked.sort((a, b) => b.comp - a.comp);
}

